"""
Simulation for the funding-proposal demo:
"Deviations from autonomous behaviour as signals of external perturbations"
realised on the BudgetODMR CW FM-lock-in NV magnetometer.

The physical system is the existing two-point/side-of-fringe frequency lock
(smcv/odmr_fieldlock_fm_pc.py) viewed as a DYNAMICAL SYSTEM. One loop cycle is
the discrete map

    f_{k+1} = f_k - (G / D_cal) * [ R(f_k - c) - R_0 ]                    (1)

where R(x) is the FM lock-in lobe shape (|dL/df| of the ODMR line), c the line
centre, R_0 the setpoint on a lobe flank and D_cal the slope measured at
calibration time. Linearising around the fixed point, the error obeys
eps_{k+1} = (1 - G_eff) eps_k with the DIMENSIONLESS loop gain

    G_eff = G * R'(x*) / D_cal .                                          (2)

|1 - G_eff| < 1 is a stable lock; at G_eff = 2 the map undergoes a flip
(period-doubling) bifurcation and the loop becomes an AUTONOMOUS self-
oscillator -- a limit cycle generated by the device itself, no external clock
or drive, cascading to chaos at higher gain (like a driven Josephson junction).

Sensing principle demonstrated here:
  * a UNIFORM field only translates the line -> the lock re-centres, the
    dynamics are invariant (shift symmetry) -> DC field = mean of the loop;
  * anything that changes the lobe SLOPE -- a field GRADIENT across the
    ensemble (inhomogeneous broadening), MW/laser power, temperature/linewidth,
    unresolved fast AC fields -- changes G_eff and therefore moves the
    oscillation ONSET: the appearance/disappearance of the autonomous limit
    cycle is the sensor output (cf. Josephson bifurcation amplifier);
  * time-periodic perturbations imprint themselves on the limit-cycle
    STATISTICS (cycle-amplitude envelope, two-time correlations).

Model assumptions: Lorentzian line of fixed contrast, so the FM lobe amplitude
and slope scale as 1/(1+beta) when the linewidth is broadened by (1+beta);
Gaussian readout noise per cycle. Parameters mirror the real rig (lobe peak
~10 mV, per-reading noise ~0.3 mV, 20 cycles/s).

Run:  python autonomous_nv_sim.py     (writes fig1..fig5.png next to itself)
"""

import os

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np

HERE = os.path.dirname(os.path.abspath(__file__))
rng = np.random.default_rng(11)

# ---- rig-like parameters -------------------------------------------------
HW      = 0.75        # ODMR half-width Gamma/2 [MHz]
RPK     = 0.010       # FM lobe peak [V] (as on the rig, ~10 mV)
SIG_R   = 3e-4        # readout noise per cycle [V] (~3 % of peak)
DT      = 0.05        # loop cycle time [s] (~20 cycles/s on the rig)
CLIP    = 8.0         # actuator range [MHz] (sweep-span clip)


def lobe(x, beta=0.0):
    """FM lock-in lobe |dL/df| for a Lorentzian of fixed contrast whose width
    is broadened by (1+beta). Positive x = detuning from the line centre."""
    hw = HW * (1.0 + beta)
    s = 2 * hw**2 * np.abs(x) / (x**2 + hw**2) ** 2
    s0 = 2 * HW**2 * (HW / np.sqrt(3)) / ((HW**2 / 3 + HW**2) ** 2)  # peak, beta=0
    return RPK * s / s0


# ---- calibration (done once, at beta = 0, like the real script) ----------
_xg = np.linspace(HW / np.sqrt(3), 6 * HW, 4000)          # outer flank
X0 = float(_xg[np.argmin(np.abs(lobe(_xg) - 0.45 * RPK))])  # setpoint: 45 % of peak
R0 = float(lobe(X0))
_d = 1e-5
D_CAL = float((lobe(X0 + _d) - lobe(X0 - _d)) / (2 * _d))   # V/MHz (<0)


def step(u, G, beta=0.0, noise=0.0):
    """One loop cycle: u is the detuning f - c."""
    R = lobe(u, beta) + noise
    return u - (G / D_CAL) * (R - R0)


def orbit(G, n=400, beta=0.0, sigma=0.0, u0=X0 + 0.05, rng=rng, drift=None):
    # note: u0 is seeded slightly OFF the fixed point, so an unstable lock
    # actually departs even in the noise-free (deterministic) runs
    """Iterate the map; returns the detuning trace. `drift` = per-step line-
    centre displacement array (enters as an equivalent detuning kick)."""
    u = np.empty(n)
    u[0] = u0
    for k in range(1, n):
        nz = sigma * rng.standard_normal() if sigma else 0.0
        u[k] = step(u[k - 1], G, beta, nz)
        if drift is not None:
            u[k] -= drift[k] - drift[k - 1]
        u[k] = np.clip(u[k], -CLIP, CLIP)
    return u


def orbit_amp(G, beta=0.0, sigma=0.0, n=600, keep=200, rng=rng):
    """Steady-state oscillation amplitude (std of the tail of the orbit)."""
    return float(np.std(orbit(G, n, beta, sigma, rng=rng)[-keep:]))


# ===========================================================================
# Fig 1: the system -- lobe + setpoint, and the map (cobweb) below/above onset
# ===========================================================================
def cobweb(ax, G, n=40, u0=None):
    xs = np.linspace(0.2, 3.2, 600)
    ax.plot(xs, [step(x, G) for x in xs], color="C0", lw=1.4,
            label=r"map $f_{k+1}(f_k)$")
    ax.plot(xs, xs, color="0.6", lw=1, ls="--", label="identity")
    u = u0 if u0 is not None else X0 + 0.55
    px, py = [u], [u]
    for _ in range(n):
        v = step(u, G)
        px += [u, v]
        py += [v, v]
        u = v
    ax.plot(px, py, color="C3", lw=0.8, alpha=0.85)
    ax.plot(X0, X0, "o", color="C2", ms=6)
    ax.set(xlabel=r"detuning $u_k$ [MHz]", ylabel=r"$u_{k+1}$ [MHz]",
           title=f"loop gain G = {G}")
    ax.grid(alpha=0.3)


fig, axes = plt.subplots(1, 3, figsize=(12, 3.6))
x = np.linspace(-4, 4, 1200)
axes[0].plot(x, 1e3 * lobe(x), color="C0", lw=1.4, label="unperturbed")
axes[0].plot(x, 1e3 * lobe(x, 0.3), color="C1", lw=1.2,
             label="slope-changing\nperturbation (+30 % width)")
axes[0].axhline(1e3 * R0, color="C2", ls=":", lw=1)
axes[0].plot(X0, 1e3 * R0, "o", color="C2", label=rf"setpoint $R_0$, $x_0$")
axes[0].set(xlabel="detuning from line centre [MHz]", ylabel="lock-in R [mV]",
            title="FM lobe = the nonlinearity of the loop")
axes[0].legend(fontsize=7)
axes[0].grid(alpha=0.3)
cobweb(axes[1], 1.0)
axes[1].set_title("G = 1.0: stable lock (tracker)")
cobweb(axes[2], 2.4)
axes[2].set_title("G = 2.4: autonomous limit cycle")
axes[1].legend(fontsize=7)
fig.tight_layout()
fig.savefig(os.path.join(HERE, "fig1_system_and_map.png"), dpi=150)
plt.close(fig)

