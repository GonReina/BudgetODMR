# ===========================================================================
# Fig 4: with realistic noise -- P(oscillating) is a sharp discriminator
# ===========================================================================
G_OP = 2.35                      # operate slightly above the beta=0 threshold
def p_osc(beta, n_trials=60):
    amp0 = orbit_amp(1.0, sigma=SIG_R)          # noise-only reference
    hits = 0
    for _ in range(n_trials):
        if orbit_amp(G_OP, beta=beta, sigma=SIG_R) > 4 * amp0:
            hits += 1
    return hits / n_trials

bet4 = np.linspace(0, 0.30, 13)
p = [p_osc(b) for b in bet4]
fig, ax = plt.subplots(figsize=(6.4, 3.8))
ax.plot(bet4, p, "o-", color="C0")
ax.set(xlabel=r"line broadening $\beta$ (perturbation strength)",
       ylabel="P(self-oscillation) over repeated trials",
       title=f"Noisy threshold detection at fixed G = {G_OP}")
ax.grid(alpha=0.3)
fig.tight_layout()
fig.savefig(os.path.join(HERE, "fig4_threshold_detection.png"), dpi=150)
plt.close(fig)

# ===========================================================================
# Fig 5: time-periodic perturbation read out from the limit-cycle statistics
# ===========================================================================
G5, N5, F_SIG = 2.4, 6000, 7.0            # Hz signal, above loop bandwidth
t5 = np.arange(N5) * DT
def envelope(u, m=1):
    """Amplitude of the period-2 limit cycle: demodulate the alternating
    component s_k = (-1)^k (u_k - <u>) with a short moving average -- i.e. a
    lock-in ON the autonomous oscillation itself."""
    s = ((-1.0) ** np.arange(len(u))) * (u - np.mean(u))
    if m > 1:                       # keep m small: a boxcar of length m has
        s = np.convolve(s, np.ones(m) / m, mode="valid")   # nulls at fs/m !
    return s

for amp_sig, tag in ((0.0, "off"), (0.03, "on")):     # 30 kHz line dither @7 Hz
    drift = amp_sig * np.sin(2 * np.pi * F_SIG * t5)
    u = orbit(G5, N5, sigma=SIG_R, drift=drift)
    env = envelope(u)
    if tag == "off":
        env_off = env
    else:
        env_on = env

def asd(y, dt, n_seg=12):
    """Welch-averaged amplitude spectrum (smooth floor -> peaks stand out)."""
    y = np.asarray(y, float) - np.mean(y)
    seg = len(y) // n_seg
    w = np.hanning(seg)
    S = []
    for k in range(n_seg):
        x = y[k * seg:(k + 1) * seg]
        S.append((np.abs(np.fft.rfft((x - x.mean()) * w)) / np.sum(w) * 2) ** 2)
    return np.fft.rfftfreq(seg, dt), np.sqrt(np.mean(S, axis=0))

fig, (ax, ax2) = plt.subplots(1, 2, figsize=(10, 3.8))
u_show = orbit(G5, 80, sigma=SIG_R)
ax.plot(np.arange(80) * DT, u_show, "o-", ms=3, lw=0.7, color="C0")
ax.set(xlabel="time [s]", ylabel="detuning $u_k$ [MHz]",
       title=f"Autonomous limit cycle (G = {G5}): period-2 hopping")
ax.grid(alpha=0.3)
fr_off, S_off = asd(env_off, DT)
fr_on, S_on = asd(env_on, DT)
ax2.semilogy(fr_off[1:], S_off[1:], lw=1.1, color="0.55", label="perturbation off")
ax2.semilogy(fr_on[1:], S_on[1:], lw=1.1, color="C3", label="7 Hz field on")
f_mix = 1.0 / (2 * DT) - F_SIG          # period-2 cycle = internal LO at Nyquist
ax2.axvline(f_mix, color="C3", ls=":", lw=1)
ax2.annotate(f"mixing sideband\n{1/(2*DT):.0f} - {F_SIG:.0f} = {f_mix:.0f} Hz:\n"
             "the limit cycle acts as\nan internal local oscillator",
             (f_mix + 0.25, 0.06), color="C3", fontsize=8)
ax2.set(xlabel="frequency [Hz]", ylabel="demodulated cycle spectrum [MHz]",
        title="Signal read out from the oscillation STATISTICS")
ax2.legend(fontsize=8)
ax2.grid(alpha=0.3)
fig.tight_layout()
fig.savefig(os.path.join(HERE, "fig5_statistics_readout.png"), dpi=150)
plt.close(fig)

